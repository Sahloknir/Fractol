#include "libft.h"

int		ft_atoi(const char *str)
{
	unsigned int	nb;
	int				i;
	int				isneg;

	i = 0;
	nb = 0;
	isneg = 1;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n'
		|| str[i] == '\r' || str[i] == '\v' || str[i] == '\f')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		isneg = (str[i] == '-') ? -1 : 1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = (nb * 10) + (str[i] - '0');
		i++;
	}
	return ((int)(nb * isneg));
}
