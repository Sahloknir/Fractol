#include "libft.h"

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	int				i;
	unsigned char	*a;
	unsigned char	*b;

	i = 0;
	a = (unsigned char *)s1;
	b = (unsigned char *)s2;
	if (n < 1)
		return (0);
	while (*a == *b && n > 1)
	{
		a++;
		b++;
		n--;
	}
	return ((int)*a - (int)*b);
}
